# Typecasting 
# Typecasting is the process of converting a variable from one data type to another.
# It is also known as explicit type conversion.
# Typecasting is used to convert a variable from one data type to another.
# For example, we can convert an integer to a float, a string to an integer, etc


#  to find the type of a variable
var = 1
print(type(var))

var2 = 1.1
print(type(var2))


var3 = "Shubham"
print(type(var3))


typecast = 3
typecaster = str(typecast)
print(type(typecaster))