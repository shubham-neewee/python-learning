# String functions

name = "Siddharth"

# len() function returns the length of a string
print(len(name))

# .endswith() function returns True if the string ends with the specified value, otherwise False
print(name.endswith("th"))

# .startswith() function returns True if the string starts with the specified value, otherwise False
print(name.startswith("Sid"))

# .capitalise() function converts the first character of a string to uppercase and makes all other characters in the string lowercase
print(name.capitalize())

# .find() function returns the index of the first occurrence of the specified value
print(name.find("d"))

# .replace() function replaces the specified value with a specified value
print(name.replace("Siddharth", "Siddharth Kumar"))


