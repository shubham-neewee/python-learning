# Write a program to sum a list of four numbers

number_list = (1,2,3,4) # Define a list of numbers
sum_of_numbers = 0 # Initialize a variable to hold the sum

n1 = number_list[0] # Get the first number from the list
n2 = number_list[1] # Get the second number from the list
n3 = number_list[2] # Get the third number from the list
n4 = number_list[3] # Get the fourth number from the list

sum_of_numbers = n1 + n2 + n3 + n4 # Add the numbers together

print(sum_of_numbers) # Print the sum of the numbers